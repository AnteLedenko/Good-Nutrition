document.getElementById('whole-grains').addEventListener('click',function(event){
    event.preventDefault();
    const wholeGrains = document.getElementById('whole-grains-source');
    if(wholeGrains.textContent === ''){
        wholeGrains.textContent = 'Brown rice, oats, farro, and barley'
    }else{
        wholeGrains.textContent = '';
    }
});

